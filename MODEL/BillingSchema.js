const mongoose = require("mongoose");

const billingSchema = new mongoose.Schema({
  userName: {
    type: String,
    required: true,
  },
  userContact: {
    type: String,
    required: true,
  },
  items: [
    {
      product: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "Product",
        required: true,
      },
      quantity: {
        type: Number,
        required: true,
        min: 1,
      },
      price: {
        type: Number,
        required: true,
      },
    },
  ],
  totalAmount: {
    type: Number,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const Billing = mongoose.model("Billing", billingSchema);
module.exports = Billing;
